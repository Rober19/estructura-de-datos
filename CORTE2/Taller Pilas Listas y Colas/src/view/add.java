    private void btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AddActionPerformed
        if (txt_Nombre.getText().isEmpty() || txt_Nombre.getText() == null
                || txt_Peso.getText().isEmpty() || txt_Peso.getText() == null) {
            JOptionPane.showMessageDialog(null, "Todos los campos son requeridos", "Error", 0);
        } else {
            try {
                if (Double.parseDouble(txt_Peso.getText()) <= 0.0) {
                    JOptionPane.showMessageDialog(null, "Pesos mayores a 0", "Error", 0);
                } else {
                    Cont.CrearPersona(new Persona(
                            txt_Nombre.getText(), UUID.randomUUID().toString(),
                            Double.parseDouble(txt_Peso.getText())));
                    View1.Listar((DefaultTableModel) View1.tbl_Disponible.getModel(), Cont.ReadPersona());
                    this.dispose();
                }
            } catch (HeadlessException | NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Debe ser un numero", "Error", 0);
            }
        }
    }//GEN-LAST:event_btn_AddActionPerformed

    
